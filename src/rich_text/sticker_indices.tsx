export function stickerUrl(index: string): undefined | {
    url: string,
    x: number,
    y: number,
    w: number,
    h: number,
} {
    const [ns, name] = index.split(".", 2);
    if (StickerDatabase[ns] && StickerDatabase[ns].stickers[name]) {
        return {url: StickerDatabase[ns].url, ...StickerDatabase[ns].stickers[name]}
    }
    return undefined;
}